# Indexing ( Access Single Item )

myString = "I Love swift"

print(myString[0])
print(myString[-1])
print(myString[3] + myString[4])

# Slicing ( Access Multiple Sequence Items )
# [Start:End] End Not Included
# [Start:End:Steps]

print(myString[:6])
print(myString[2:6])
print(myString[:6])
print(myString[::])
print(myString[::2])  



